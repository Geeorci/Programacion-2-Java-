public class User{
    String name;
    String lastName;
    String lastNameII;
    String nickname;
    String bird;
    String email;
    String photo;
    char gender;
    int id;

    public User(String name, String lastName, String lastNameII, String nickName, String bird, String email, String photho, char gender, int id ){
            this.name = name;
            this.lastName = lastName;
            this.lastNameII = lastNameII;
            this.nickname = nickName;
            this.bird = bird;
            this.email = email;
            this.photo = photho;
            this.gender = gender;
            this.id = id;
    }
}
